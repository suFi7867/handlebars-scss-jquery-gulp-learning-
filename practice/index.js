$(document).ready(function () {
    var Template = $("#DesignedForYou-template").html();
    var compiledUsersTemp = Handlebars.compile(Template);
    var $tabs = $(".secondary-tab");
    var $tabContents = $(".right-content");
  
    $tabs.on("click", function () {
      var tabIndex = $(this).data("index");
      
      // Hide all tab contents
      $tabContents.removeClass("active-tab-content").addClass("inactive-tab-content");
      
      // Show the selected tab content
      $tabContents.eq(tabIndex).removeClass("inactive-tab-content").addClass("active-tab-content");
  
      // Update the active class on tabs
      $tabs.removeClass("is-active");
      $(this).addClass("is-active");
    });
  
    $(".DesignedForYou").html(compiledUsersTemp(userData));
  });
  